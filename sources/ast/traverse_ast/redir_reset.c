#include "exec.h"

void			redir_reset(void)
{
	extern int	g_fd[3];

	dup2(g_fd[0], 0);
	close(g_fd[0]);
	dup2(g_fd[1], 1);
	close(g_fd[1]);
	dup2(g_fd[2], 2);
	close(g_fd[2]);
}
