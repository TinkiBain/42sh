#include "lexer.h"

size_t			lexer_and_or(char *str, t_lex **lex)
{
	if (*str == '&' && *(str + 1) && *(str + 1) == '&')
		(*lex)->type = AND;
	else if (*str == '|' && *(str + 1) && *(str + 1) == '|')
		(*lex)->type = OR;
	else
		return (0);
	return (2);
}
